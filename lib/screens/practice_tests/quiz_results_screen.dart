import 'package:flutter/material.dart';
import 'package:firebase_auth/firebase_auth.dart';
import '../../theme/app_theme.dart';
import '../../models/practice_test_model.dart';
import '../../models/question_model.dart';
import '../../models/test_result_model.dart';
import '../../services/test_result_service.dart';
import 'practice_tests_screen.dart';
import 'quiz_screen.dart';

class QuizResultsScreen extends StatefulWidget {
  final PracticeTestModel practiceTest;
  final Map<int, int?> selectedAnswers;
  final int score;
  final int totalQuestions;

  const QuizResultsScreen({
    super.key,
    required this.practiceTest,
    required this.selectedAnswers,
    required this.score,
    required this.totalQuestions,
  });

  @override
  State<QuizResultsScreen> createState() => _QuizResultsScreenState();
}

class _QuizResultsScreenState extends State<QuizResultsScreen> {
  bool _showExplanations = false;
  bool _isSaving = false;
  final TestResultService _testResultService = TestResultService();

  double get _percentage => (widget.score / widget.totalQuestions) * 100;

  @override
  void initState() {
    super.initState();
    _saveTestResult();
  }

  Future<void> _saveTestResult() async {
    final user = FirebaseAuth.instance.currentUser;
    if (user == null) return;

    setState(() {
      _isSaving = true;
    });

    try {
      final result = TestResultModel(
        id: '', // Will be generated by Firestore
        userId: user.uid,
        testId: widget.practiceTest.id,
        testTitle: widget.practiceTest.title,
        eventName: widget.practiceTest.eventName,
        score: widget.score,
        totalQuestions: widget.totalQuestions,
        percentage: _percentage,
        selectedAnswers: widget.selectedAnswers,
        completedAt: DateTime.now(),
      );

      await _testResultService.saveTestResult(result);
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Error saving result: $e'),
            backgroundColor: AppTheme.error,
          ),
        );
      }
    } finally {
      if (mounted) {
        setState(() {
          _isSaving = false;
        });
      }
    }
  }

  Color get _scoreColor {
    if (_percentage >= 80) return AppTheme.success;
    if (_percentage >= 60) return AppTheme.warning;
    return AppTheme.error;
  }

  String get _scoreMessage {
    if (_percentage >= 90) return 'Excellent!';
    if (_percentage >= 80) return 'Great Job!';
    if (_percentage >= 70) return 'Good Work!';
    if (_percentage >= 60) return 'Not Bad!';
    return 'Keep Practicing!';
  }

  @override
  Widget build(BuildContext context) {
    final isDark = Theme.of(context).brightness == Brightness.dark;

    return Scaffold(
      backgroundColor: isDark ? AppTheme.darkBackground : AppTheme.background,
      appBar: AppBar(title: const Text('Quiz Results'), elevation: 0),
      body: Column(
        children: [
          // Score Section
          Container(
            padding: const EdgeInsets.all(24),
            decoration: BoxDecoration(
              color: isDark ? AppTheme.darkSurface : Colors.white,
              border: Border(
                bottom: BorderSide(
                  color: isDark
                      ? Colors.white.withValues(alpha: 0.1)
                      : Colors.black.withValues(alpha: 0.05),
                ),
              ),
            ),
            child: Column(
              children: [
                if (_isSaving)
                  Padding(
                    padding: const EdgeInsets.only(bottom: 16),
                    child: Row(
                      mainAxisAlignment: MainAxisAlignment.center,
                      children: [
                        SizedBox(
                          width: 16,
                          height: 16,
                          child: CircularProgressIndicator(
                            strokeWidth: 2,
                            valueColor: AlwaysStoppedAnimation<Color>(
                              AppTheme.primaryBlue,
                            ),
                          ),
                        ),
                        const SizedBox(width: 8),
                        Text(
                          'Saving results...',
                          style: TextStyle(
                            fontSize: 12,
                            color: isDark
                                ? Colors.white.withValues(alpha: 0.7)
                                : AppTheme.darkGray,
                          ),
                        ),
                      ],
                    ),
                  ),
                // Score Circle
                Container(
                  width: 120,
                  height: 120,
                  decoration: BoxDecoration(
                    shape: BoxShape.circle,
                    color: _scoreColor.withValues(alpha: 0.1),
                    border: Border.all(color: _scoreColor, width: 4),
                  ),
                  child: Center(
                    child: Column(
                      mainAxisAlignment: MainAxisAlignment.center,
                      children: [
                        Text(
                          '${widget.score}',
                          style: TextStyle(
                            fontSize: 36,
                            fontWeight: FontWeight.bold,
                            color: _scoreColor,
                          ),
                        ),
                        Text(
                          '/ ${widget.totalQuestions}',
                          style: TextStyle(
                            fontSize: 18,
                            color: isDark
                                ? Colors.white.withValues(alpha: 0.6)
                                : AppTheme.mediumGray,
                          ),
                        ),
                      ],
                    ),
                  ),
                ),
                const SizedBox(height: 20),
                Text(
                  _scoreMessage,
                  style: TextStyle(
                    fontSize: 24,
                    fontWeight: FontWeight.bold,
                    color: isDark ? Colors.white : AppTheme.black,
                  ),
                ),
                const SizedBox(height: 8),
                Text(
                  '${_percentage.toStringAsFixed(1)}% Correct',
                  style: TextStyle(
                    fontSize: 16,
                    color: isDark
                        ? Colors.white.withValues(alpha: 0.7)
                        : AppTheme.darkGray,
                  ),
                ),
              ],
            ),
          ),

          // Toggle Button
          Padding(
            padding: const EdgeInsets.all(16),
            child: ElevatedButton.icon(
              onPressed: () {
                setState(() {
                  _showExplanations = !_showExplanations;
                });
              },
              icon: Icon(
                _showExplanations
                    ? Icons.visibility_off_rounded
                    : Icons.visibility_rounded,
              ),
              label: Text(
                _showExplanations ? 'Hide Explanations' : 'Show Explanations',
              ),
              style: ElevatedButton.styleFrom(
                backgroundColor: AppTheme.primaryBlue,
                padding: const EdgeInsets.symmetric(
                  horizontal: 24,
                  vertical: 14,
                ),
              ),
            ),
          ),

          // Questions Review
          Expanded(
            child: ListView.builder(
              padding: const EdgeInsets.symmetric(horizontal: 16),
              itemCount: widget.practiceTest.questions.length,
              itemBuilder: (context, index) {
                final question = widget.practiceTest.questions[index];
                final selectedAnswer = widget.selectedAnswers[index];
                final isCorrect = selectedAnswer == question.correctAnswerIndex;
                return _buildQuestionReview(
                  question,
                  index,
                  selectedAnswer,
                  isCorrect,
                  isDark,
                );
              },
            ),
          ),

          // Action Buttons
          Container(
            padding: const EdgeInsets.all(20),
            decoration: BoxDecoration(
              color: isDark ? AppTheme.darkSurface : Colors.white,
              border: Border(
                top: BorderSide(
                  color: isDark
                      ? Colors.white.withValues(alpha: 0.1)
                      : Colors.black.withValues(alpha: 0.05),
                ),
              ),
            ),
            child: SafeArea(
              child: Row(
                children: [
                  Expanded(
                    child: OutlinedButton(
                      onPressed: () {
                        Navigator.pushReplacement(
                          context,
                          MaterialPageRoute(
                            builder: (context) => const PracticeTestsScreen(),
                          ),
                        );
                      },
                      style: OutlinedButton.styleFrom(
                        padding: const EdgeInsets.symmetric(vertical: 14),
                        side: BorderSide(
                          color: isDark
                              ? AppTheme.darkPrimary
                              : AppTheme.primaryBlue,
                        ),
                      ),
                      child: Text(
                        'Back to Tests',
                        style: TextStyle(
                          color: isDark
                              ? AppTheme.darkPrimary
                              : AppTheme.primaryBlue,
                        ),
                      ),
                    ),
                  ),
                  const SizedBox(width: 12),
                  Expanded(
                    child: ElevatedButton(
                      onPressed: () {
                        Navigator.pushReplacement(
                          context,
                          MaterialPageRoute(
                            builder: (context) =>
                                QuizScreen(practiceTest: widget.practiceTest),
                          ),
                        );
                      },
                      style: ElevatedButton.styleFrom(
                        padding: const EdgeInsets.symmetric(vertical: 14),
                        backgroundColor: AppTheme.primaryBlue,
                      ),
                      child: const Text(
                        'Retake Quiz',
                        style: TextStyle(
                          color: Colors.white,
                          fontWeight: FontWeight.w600,
                        ),
                      ),
                    ),
                  ),
                ],
              ),
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildQuestionReview(
    QuestionModel question,
    int index,
    int? selectedAnswer,
    bool isCorrect,
    bool isDark,
  ) {
    return Container(
      margin: const EdgeInsets.only(bottom: 16),
      padding: const EdgeInsets.all(16),
      decoration: BoxDecoration(
        color: isDark ? AppTheme.darkSurface : Colors.white,
        borderRadius: BorderRadius.circular(16),
        border: Border.all(
          color: isCorrect
              ? AppTheme.success.withValues(alpha: 0.5)
              : AppTheme.error.withValues(alpha: 0.5),
          width: 2,
        ),
        boxShadow: [
          BoxShadow(
            color: Colors.black.withValues(alpha: isDark ? 0.2 : 0.06),
            blurRadius: 12,
            offset: const Offset(0, 4),
          ),
        ],
      ),
      child: Column(
        crossAxisAlignment: CrossAxisAlignment.start,
        children: [
          // Question Header
          Row(
            children: [
              Container(
                padding: const EdgeInsets.symmetric(
                  horizontal: 10,
                  vertical: 6,
                ),
                decoration: BoxDecoration(
                  color: isCorrect
                      ? AppTheme.success.withValues(alpha: 0.1)
                      : AppTheme.error.withValues(alpha: 0.1),
                  borderRadius: BorderRadius.circular(8),
                ),
                child: Row(
                  mainAxisSize: MainAxisSize.min,
                  children: [
                    Icon(
                      isCorrect ? Icons.check_circle : Icons.cancel,
                      size: 16,
                      color: isCorrect ? AppTheme.success : AppTheme.error,
                    ),
                    const SizedBox(width: 6),
                    Text(
                      isCorrect ? 'Correct' : 'Incorrect',
                      style: TextStyle(
                        fontSize: 12,
                        fontWeight: FontWeight.w600,
                        color: isCorrect ? AppTheme.success : AppTheme.error,
                      ),
                    ),
                  ],
                ),
              ),
              const Spacer(),
              Text(
                'Question ${index + 1}',
                style: TextStyle(
                  fontSize: 12,
                  color: isDark
                      ? Colors.white.withValues(alpha: 0.6)
                      : AppTheme.mediumGray,
                ),
              ),
            ],
          ),
          const SizedBox(height: 12),

          // Question Text
          Text(
            question.question,
            style: TextStyle(
              fontSize: 16,
              fontWeight: FontWeight.bold,
              color: isDark ? Colors.white : AppTheme.black,
            ),
          ),
          const SizedBox(height: 16),

          // Options
          ...List.generate(question.options.length, (optionIndex) {
            final isSelected = selectedAnswer == optionIndex;
            final isCorrectAnswer = optionIndex == question.correctAnswerIndex;
            Color? optionColor;
            IconData? optionIcon;

            if (isCorrectAnswer) {
              optionColor = AppTheme.success;
              optionIcon = Icons.check_circle;
            } else if (isSelected && !isCorrect) {
              optionColor = AppTheme.error;
              optionIcon = Icons.cancel;
            }

            return Container(
              margin: const EdgeInsets.only(bottom: 8),
              padding: const EdgeInsets.all(12),
              decoration: BoxDecoration(
                color: isCorrectAnswer
                    ? AppTheme.success.withValues(alpha: 0.1)
                    : (isSelected && !isCorrect)
                    ? AppTheme.error.withValues(alpha: 0.1)
                    : (isDark ? AppTheme.darkCard : AppTheme.lightGray)
                          .withValues(alpha: 0.3),
                borderRadius: BorderRadius.circular(8),
                border: Border.all(
                  color:
                      optionColor?.withValues(alpha: 0.5) ?? Colors.transparent,
                  width: optionColor != null ? 2 : 0,
                ),
              ),
              child: Row(
                children: [
                  if (optionIcon != null)
                    Icon(optionIcon, size: 20, color: optionColor),
                  if (optionIcon != null) const SizedBox(width: 12),
                  Expanded(
                    child: Text(
                      question.options[optionIndex],
                      style: TextStyle(
                        fontSize: 14,
                        fontWeight: isCorrectAnswer || isSelected
                            ? FontWeight.w600
                            : FontWeight.normal,
                        color: isDark ? Colors.white : AppTheme.black,
                      ),
                    ),
                  ),
                  if (isCorrectAnswer)
                    Text(
                      'Correct',
                      style: TextStyle(
                        fontSize: 12,
                        fontWeight: FontWeight.w600,
                        color: AppTheme.success,
                      ),
                    ),
                ],
              ),
            );
          }),

          // Explanation
          if (_showExplanations) ...[
            const SizedBox(height: 16),
            Container(
              padding: const EdgeInsets.all(12),
              decoration: BoxDecoration(
                color: AppTheme.primaryBlue.withValues(alpha: 0.1),
                borderRadius: BorderRadius.circular(8),
              ),
              child: Row(
                crossAxisAlignment: CrossAxisAlignment.start,
                children: [
                  Icon(
                    Icons.lightbulb_outline_rounded,
                    color: AppTheme.primaryBlue,
                    size: 20,
                  ),
                  const SizedBox(width: 12),
                  Expanded(
                    child: Column(
                      crossAxisAlignment: CrossAxisAlignment.start,
                      children: [
                        Text(
                          'Explanation:',
                          style: TextStyle(
                            fontSize: 13,
                            fontWeight: FontWeight.w600,
                            color: AppTheme.primaryBlue,
                          ),
                        ),
                        const SizedBox(height: 4),
                        Text(
                          question.explanation,
                          style: TextStyle(
                            fontSize: 13,
                            color: isDark
                                ? Colors.white.withValues(alpha: 0.8)
                                : AppTheme.darkGray,
                          ),
                        ),
                      ],
                    ),
                  ),
                ],
              ),
            ),
          ],
        ],
      ),
    );
  }
}
